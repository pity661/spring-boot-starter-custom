public class ASCII {

  public static void main(String[] args) {
    // 48
    System.out.println((int) '0');
    // 97
    System.out.println((int) 'a');
    // 65
    System.out.println((int) 'A');
    // 49
    System.out.println('a' - '0');
    // 32
    System.out.println('a' - 'A');
    // 25
    System.out.println('z' - 'a');
  }
}
